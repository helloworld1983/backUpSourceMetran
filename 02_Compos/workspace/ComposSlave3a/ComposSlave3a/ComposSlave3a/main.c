/******************************************************************************
##
## Name    :main
## Function:メインルーチン
## Input   :なし
## Output  :なし
## Memo    :なし
##
******************************************************************************/
void main(void)
{
	clock_init();
	PWM8_1_EnableInt();
	I2C_CNT_init();
	LED_CNT_init();
	LED_1_Start();
	PWM8_1_Start();
	M8C_EnableGInt;
	
	
	while(1)
	{
		//////////////////////////////////////////
		// 100msec単位で実行する処理            //
		//////////////////////////////////////////
		if(flag100ms != 0)						// シリアル送受信低レベル処理
		{
			flag100ms = 0;                      // 100msec周期フラグクリア
			
			LED_1_On();
			I2C_CNT();
			LED_1_Off();
		}
		
		//////////////////////////////////////////
		// 10msec単位で実行する処理             //
		//////////////////////////////////////////
		if(flag10ms != 0)						//
		{
			flag10ms = 0;						// 10msec周期フラグクリア
			
		}
		
		//////////////////////////////////////////
		// 0.5msec単位で実行する処理            //
		//////////////////////////////////////////
		else if(flag05ms != 0)					//
		{
			
			flag05ms = 0;						// 0.5msec周期フラグクリア
			
			LED_1_On();
			LED_CNT();
			LED_1_Off();
		}
		
	}
}
